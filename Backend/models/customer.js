import mongoose from "mongoose";

const customerSchema = mongoose.Schema({
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  username: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    minLength: 5,
  },
  password: {
    type: String,
    required: true,
    minLength: 8,
  },
  firstName: {
    type: String,
    required: true,
    trim: true,
    maxLength: 50,
  },
  lastName: {
    type: String,
    required: true,
    trim: true,
    maxLength: 50,
  },
  address: {
    type: String,
    required: true,
    trim: true,
    maxLength: 255,
  },
});

export const Customer = mongoose.model("Customer", customerSchema);
